n = int(input())

def triangle(n):
      from math import factorial
      list1 = []
      for i in range(n // 2 + 1):
            list1.append(int(factorial(n) / (factorial(i) * factorial(n - i))))
      if n != 0:
            list1.extend(list1[len(list1) - 2 + (n % 2)::-1])
      return list1

print(triangle(n))
