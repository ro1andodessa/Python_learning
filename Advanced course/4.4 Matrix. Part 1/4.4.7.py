# üü¢üü¢üü¢üü¢üü¢üü¢ Main üü¢üü¢üü¢üü¢üü¢üü¢
n = int(input())
matrix = []
sum_up, sum_right, sum_down, sum_left = 0, 0, 0, 0

for i in range(n):
    matrix.append([int(num) for num in input().split()])

for i in range(n):
    for j in range(n):
        if (i < j and i < (n - 1 - j)):
            sum_up += matrix[i][j]
        elif (i < j and i > (n - 1 - j)):
            sum_right += matrix[i][j]
        elif (i > j and i > (n - 1 - j)):
            sum_down += matrix[i][j]
        elif (i > j and i < (n - 1 - j)):
            sum_left += matrix[i][j]

print('–í–µ—Ä—Ö–Ω—è—è —á–µ—Ç–≤–µ—Ä—Ç—å:', sum_up)
print('–ü—Ä–∞–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å:', sum_right)
print('–ù–∏–∂–Ω—è—è —á–µ—Ç–≤–µ—Ä—Ç—å:', sum_down)
print('–õ–µ–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å:', sum_left)


