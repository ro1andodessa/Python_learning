# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Ç–µ–∫—Å—Ç –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å —É—Å–ª–æ–≤–∏–µ–º –∑–∞–¥–∞—á–∏.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –≠–ª–µ–º–µ–Ω—Ç—ã –¥–∏–∞–≥–æ–Ω–∞–ª–µ–π –Ω–µ —É—á–∏—Ç—ã–≤–∞—é—Ç—Å—è.

# –¢–µ—Å—Ç–æ–≤—ã–µ –¥–∞–Ω–Ω—ã–µ üü¢
# Sample Input 1:
# 4
# 1 2 3 4
# 5 6 7 8
# 3 4 5 6
# 1 2 3 4
# Sample Output 1:
# –í–µ—Ä—Ö–Ω—è—è —á–µ—Ç–≤–µ—Ä—Ç—å: 5
# –ü—Ä–∞–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å: 14
# –ù–∏–∂–Ω—è—è —á–µ—Ç–≤–µ—Ä—Ç—å: 5
# –õ–µ–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å: 8

# Sample Input 2:
# 5
# 1 4 3 4 7
# 5 6 7 8 4
# 3 8 5 6 1
# 1 2 9 4 8
# 5 6 1 5 8
# Sample Output 2:
# –í–µ—Ä—Ö–Ω—è—è —á–µ—Ç–≤–µ—Ä—Ç—å: 18
# –ü—Ä–∞–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å: 19
# –ù–∏–∂–Ω—è—è —á–µ—Ç–≤–µ—Ä—Ç—å: 21
