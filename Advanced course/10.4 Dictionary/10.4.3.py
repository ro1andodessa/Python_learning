dict1, dict2 = {}, {}
for i in input().lower().split():
    for j in i.strip('.,!?:;-'):
        dict1[j] = dict1.get(j, 0) +1

for i in input().lower().split():
    for j in i.strip('.,!?:;-'):
        dict2[j] = dict2.get(j, 0) +1

print('YES' if dict1 == dict2 else 'NO')

# –†–µ—à–µ–Ω–∏–µ 2

def s(word):
    res = {}
    for i in word.lower():
        if i.isalpha():
            res[i] = res.get(i, 0) + 1
    return res


print(("NO", "YES")[s(input()) == s(input())])